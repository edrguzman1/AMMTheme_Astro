  <script>
    function initHeroSlider() {
      const slides = document.querySelectorAll('.slide-item');
      const dots = document.querySelectorAll('.dot-btn');
      const prevBtn = document.getElementById('slider-prev');
      const nextBtn = document.getElementById('slider-next');
      if (slides.length === 0) return;
      let currentIndex = 0;
      const totalSlides = slides.length;
      let interval;
      const showSlide = (index) => {
        slides.forEach((slide, i) => {
          if (i === index) {
            slide.classList.remove('opacity-0', 'z-0');
            slide.classList.add('opacity-100', 'z-10', 'active');
          } else {
            slide.classList.remove('opacity-100', 'z-10', 'active');
            slide.classList.add('opacity-0', 'z-0');
          }
        });
        dots.forEach((dot, i) => {
          if (i === index) {
            dot.classList.remove('bg-white/60');
            dot.classList.add('bg-cyan-400', 'w-10');
          } else {
            dot.classList.remove('bg-cyan-400', 'w-10');
            dot.classList.add('bg-white/60');
          }
        });
      };
      const nextSlide = () => {
        currentIndex = (currentIndex + 1) % totalSlides;
        showSlide(currentIndex);
      };
      const prevSlide = () => {
        currentIndex = (currentIndex - 1 + totalSlides) % totalSlides;
        showSlide(currentIndex);
      };
      const startAutoPlay = () => {
        clearInterval(interval);
        interval = setInterval(nextSlide, 5000);
      };
      nextBtn?.addEventListener('click', () => {
        nextSlide();
        startAutoPlay();
      });
      prevBtn?.addEventListener('click', () => {
        prevSlide();
        startAutoPlay();
      });
      dots.forEach((dot) => {
        dot.addEventListener('click', (e) => {
          const target = parseInt(dot.getAttribute('data-target'));
          currentIndex = target;
          showSlide(currentIndex);
          startAutoPlay();
        });
      });
      showSlide(0);
      startAutoPlay();
    }
    initHeroSlider();
    document.addEventListener("astro:after-swap", initHeroSlider);
  </script>
